SELECT
  VW_PRODUCT_APP_STATUS.APP_ID,
  VW_PRODUCT_APP_STATUS.APP_DATE,
  VW_PRODUCT_APP_STATUS.PARTNER_NAME,
  VW_PRODUCT_APP_STATUS.CLIENT_PRODUCT_CODE,
  VW_PRODUCT_APP_STATUS.MEMBERSHIP_NUMBER,
  VW_PRODUCT_APP_STATUS.END_DATE,
  VW_PRODUCT_APP_STATUS.USR_CREATED,
  VW_PRODUCT_APP_STATUS.CREATED,
  VW_PRODUCT_APP_STATUS.USR_CHANGED,
  VW_PRODUCT_APP_STATUS.CHANGED,
  VW_CUSTOMER.PERSON_ID,
  VW_CUSTOMER.MEMBER_ID,
  VW_CUSTOMER.ACTIVE_CARD_NO,
  VW_CUSTOMER.COUNTRY_IATA_CODE
FROM
  VW_PRODUCT_APP_STATUS,
VW_CUSTOMER
where
VW_CUSTOMER.ACTIVE_CARD_NO = VW_PRODUCT_APP_STATUS.MEMBERSHIP_NUMBER